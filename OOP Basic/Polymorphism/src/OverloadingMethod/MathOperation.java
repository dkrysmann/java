package OverloadingMethod;


public class MathOperation {
    //Overloading Method
    public int addNumbers(int a,int b){
        return a + b;
    }
    public int addNumbers(int a,int b,int c){
        return a + b + c;
    }
    public int addNumbers(int a,int b,int c,int d){
        return a + b + c + d;
    }
}
